# Using input function to ask variable values during run time
user_name = input("What is your name? ")
user_location = input("What is your location? ")

# Printing data using F Method
print(f"Hello, your name is {user_name}. Your location is {user_location}.")

# Printing data using Dot Format Method
print("Hello, your name is {}. Your location is {}.".format(user_name,user_location))